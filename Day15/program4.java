/*
Concept:
HashMap stores key-value pairs.
*/

import java.util.*;
class CollectionDemo4{
    public static void main(String[] args) {
      HashMap<Integer , String> hm = new HashMap<>();
      hm.put(1, " Siddhesh");
      hm.put(2, " Santosh");
      hm.put(3, " Manisha");
      hm.put(4, " Piyush"); 

      System.out.println(hm);
    }
}
